      function geocodeAddress(geocoder, resultsMap, cncResults) {
          
        var address;

          for(var i=0; i<cncResults.length; i++)
          {
            address = cncResults[i].city + "," + cncResults[i].state;
            geocoder.geocode({'address': address}, function(results, status) {
              if (status === 'OK') {
                resultsMap.setCenter(results[0].geometry.location);
                var marker = new google.maps.Marker({
                  map: resultsMap,
                  position: results[0].geometry.location,
                  icon: {url: "/wp-content/uploads/2019/02/cnc-green.png"}
                });
              } else {
                console.log(status);
              }
            });
          }
      }

      function ajax(geocoder, resultsMap, callbackFunction){
          var url = "http://138.68.1.184/profiles/?format=json";
          var xhr = new XMLHttpRequest();
          xhr.open('GET', url);
          xhr.send();
          xhr.onreadystatechange = function () {
            console.log(xhr.readyState);
            // 2 - headers received
            // 3 - data is loading
            // 4 - everything is done and ready
            if(xhr.readyState == XMLHttpRequest.DONE){
              if(xhr.status ==200){
                callbackFunction(geocoder, resultsMap, JSON.parse(xhr.responseText));
              }
              else{
                console.log("Error! Error Status: " + xhr.status);
              }
            }
          }
      }
